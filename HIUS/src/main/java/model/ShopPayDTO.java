package model;

public class ShopPayDTO {
	String shopPayNo;
	String shopPaysum;
	String shopPaymet;
	String shopPaydate;
	String orderNo;
	
	public String getShopPayNo() {
		return shopPayNo;
	}
	public void setShopPayNo(String shopPayNo) {
		this.shopPayNo = shopPayNo;
	}
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public String getShopPaysum() {
		return shopPaysum;
	}
	public void setShopPaysum(String shopPaysum) {
		this.shopPaysum = shopPaysum;
	}
	public String getShopPaymet() {
		return shopPaymet;
	}
	public void setShopPaymet(String shopPaymet) {
		this.shopPaymet = shopPaymet;
	}
	public String getShopPaydate() {
		return shopPaydate;
	}
	public void setShopPaydate(String shopPaydate) {
		this.shopPaydate = shopPaydate;
	}
	
	
}
